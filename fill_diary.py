# Open template
doc = Document(template)
table = doc.tables[0]

# Fill rows 5-76 (the "—" rows) with our content
for i, (date, topic, content, remarks, conclusion) in enumerate(entries):
    row_idx = 5 + i  # Start from row 5
    if row_idx >= 95:  # Don't overwrite final evaluation rows
        break

    cells = table.rows[row_idx].cells
    # Date
    cells[0].text = ""
    p = cells[0].paragraphs[0]
    run = p.add_run(date)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(11)

    # Topic
    cells[1].text = ""
    p = cells[1].paragraphs[0]
    run = p.add_run(topic)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(11)

    # Content
    cells[2].text = ""
    p = cells[2].paragraphs[0]
    run = p.add_run(content)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(11)

    # Remarks
    cells[3].text = ""
    if remarks and remarks != "—":
        p = cells[3].paragraphs[0]
        run = p.add_run(remarks)
        run.font.name = 'Times New Roman'
        run.font.size = Pt(11)

    # Conclusion (keep empty for signature)
    cells[4].text = ""

doc.save(output)
