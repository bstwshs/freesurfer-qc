def add_heading_custom(text, level=1):
    """Add heading with GOST styling."""
    h = doc.add_heading(text, level=level)
    for run in h.runs:
        run.font.name = 'Times New Roman'
        rPr = run._element.get_or_add_rPr()
        rFonts = OxmlElement('w:rFonts')
        rFonts.set(qn('w:ascii'), 'Times New Roman')
        rFonts.set(qn('w:hAnsi'), 'Times New Roman')
        rFonts.set(qn('w:cs'), 'Times New Roman')
        rPr.insert(0, rFonts)
        if level == 1:
            run.font.size = Pt(16)
        elif level == 2:
            run.font.size = Pt(14)
    return h

def add_paragraph_custom(text, bold=False, italic=False, alignment=None):
    """Add paragraph with GOST styling."""
    p = doc.add_paragraph()
    run = p.add_run(text)
    run.font.name = 'Times New Roman'
    run.font.size = Pt(14)
    run.bold = bold
    run.italic = italic
    if alignment is not None:
        p.alignment = alignment
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.first_line_indent = Cm(1.25)
    return p

def add_paragraph_no_indent(text, bold=False, alignment=None):
    """Add paragraph without first-line indent (for lists, captions)."""
